import fs from 'node:fs';
import path from 'node:path';

const root = path.resolve(path.dirname(new URL(import.meta.url).pathname), '..');
const dataDir = path.join(root, 'data');
const outDir = path.join(root, 'src', 'data');

function readUtf8(fileName) {
  return fs.readFileSync(path.join(dataDir, fileName), 'utf8');
}

function parseFamilies(tsv) {
  const lines = tsv.split(/\r?\n/).filter(Boolean);
  const rows = [];
  for (let i = 1; i < lines.length; i += 1) {
    const line = lines[i];
    const [familyKeyRaw, headwordRaw, membersRaw = ''] = line.split('\t');
    const familyKey = (familyKeyRaw || '').trim().toLowerCase();
    const headword = (headwordRaw || '').trim().toLowerCase();
    if (!familyKey || !headword) continue;
    const members = membersRaw
      .split('|')
      .map((m) => m.trim().toLowerCase())
      .filter(Boolean);
    const uniqueMembers = Array.from(new Set(members));
    rows.push({ familyKey, headword, members: uniqueMembers });
  }
  return rows;
}

function parseMwes(txt) {
  return txt
    .split(/\r?\n/)
    .map((line) => line.trim().toLowerCase())
    .filter(Boolean);
}

function parseContractions(txt) {
  const rows = [];
  for (const line of txt.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed) continue;
    const [contractionRaw, expansionsRaw = ''] = trimmed.split('\t');
    const contraction = (contractionRaw || '').trim().toLowerCase();
    if (!contraction) continue;
    const expansions = expansionsRaw
      .split('|')
      .map((e) => e.trim().toLowerCase())
      .filter(Boolean);
    if (expansions.length === 0) continue;
    rows.push({ contraction, expansions: Array.from(new Set(expansions)) });
  }
  return rows;
}

function asTs(value) {
  return JSON.stringify(value, null, 2);
}

function writeSeedModule({
  languageCode,
  familiesFile,
  mwesFile,
  contractionsFile,
}) {
  const families = parseFamilies(readUtf8(familiesFile));
  const mwes = parseMwes(readUtf8(mwesFile));
  const contractions = parseContractions(readUtf8(contractionsFile));
  const upper = languageCode.toUpperCase();
  const outPath = path.join(outDir, `${languageCode}LexiconSeed.ts`);

  const output = `/* eslint-disable */
// Auto-generated by data/generate_lexicon_seed.mjs from files in /data.

export type SeedFamily = {
  familyKey: string;
  headword: string;
  members: string[];
};

export type SeedContraction = {
  contraction: string;
  expansions: string[];
};

export const ${upper}_LEXICON_FAMILIES: SeedFamily[] = ${asTs(families)};

export const ${upper}_LEXICON_MWES: string[] = ${asTs(mwes)};

export const ${upper}_LEXICON_CONTRACTIONS: SeedContraction[] = ${asTs(contractions)};
`;

  fs.writeFileSync(outPath, output, 'utf8');
  console.log(`Wrote ${outPath}`);
}

writeSeedModule({
  languageCode: 'en',
  familiesFile: 'en_families_top8000.tsv',
  mwesFile: 'en_mwe.txt',
  contractionsFile: 'en_contractions.txt',
});

writeSeedModule({
  languageCode: 'pt',
  familiesFile: 'pt_families_top8000.tsv',
  mwesFile: 'pt_mwe.txt',
  contractionsFile: 'pt_contractions.txt',
});
